package MeaningfulNames;

public class PickOneWordPerConcept {
    class Person {
        String name;

        String getName() {
            return name;
        }
    }

    class Book {
        String text;

        String getText() {
            return text;
        }
    }

    class BankAccount {
        double balance;

        double getBalance() {
            return balance;
        }
    }
}
